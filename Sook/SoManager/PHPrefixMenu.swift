            let task = base.outputFormat(forBus: 0)
            base.installTap(onBus: 0, bufferSize: 1024, format: task) { (buffer: AVAudioPCMBuffer, when: AVAudioTime) in
                self.addBegin?.append(buffer)
                
                
                guard let channelData = buffer.floatChannelData else { return }
                
                
                let enabledView = vDSP_Length(buffer.frameLength)
                var back: Float = 0
                vDSP_rmsqv(channelData[0], 1, &back, enabledView)
                
                
                let status = 20 * log10(back)
                
                
                DispatchQueue.main.async {
                    
                    let attributes = 1.0 + CGFloat((status + 50) / 50.0)
                    let picture = max(1.0, min(attributes, 1.5))
                    
                    if picture > 1.0 {
                        self.decibelScaleHandler?(Float(picture))
                    }
                }
            }
            
            self.audioEngine.prepare()
            try! self.audioEngine.start()
        }
        
    }
    
